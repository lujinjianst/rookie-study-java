		System.out.println("Max value:Byte.MAX_VALUE=" + Integer.MAX_VALUE);
		System.out.println("Min value:Byte.MIN_VALUE=" + Integer.MIN_VALUE);
		System.out.println();
		
//		long
		System.out.println("base type:long  binary equivalent:" + Long.SIZE);
		System.out.println("Wrap class:java.lang.Byte");
		System.out.println("Max value:Byte.MAX_VALUE=" + Long.MAX_VALUE);
		System.out.println("Min value:Byte.MIN_VALUE=" + Long.MIN_VALUE);
		System.out.println();
		
//		float
		System.out.println("base type:float  binary equivalent:" + Float.SIZE);
